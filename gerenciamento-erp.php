					<h4>Por que vale a pena integrá-lo?</h4>
				</div>
				<p>Entender a importância de tais sistemas é o primeiro passo que toda loja virtual deve tomar numa época em que a concorrência está sempre procurando meios de superá-la. Porém, tão importante quanto o que é ERP é entender que ele apenas será útil se estiver completamente integrado ao sistema do e-commerce.</p>

				<p>As plataformas de mercado geralmente possuem API’s de integração, assim como os ERP’s possuem suas próprias API’s para integração. Conforme as necessidades do cliente, pode-se ter uma integração do ERP com a plataforma de e-commerce, ou vice-versa. Não existe o melhor sentido, o importante é centralizar as informações. As mesmas devem ser automáticas, isto é, transparentes para o gestor do negócio, que deve se preocupar com as vendas. A “expertise” fica a cargo dos profissionais do sistema integrado de gestão.</p>

